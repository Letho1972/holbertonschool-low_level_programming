#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/**
 * main - positif or negatif
 * Return: success
 */

int main(void)
{
	char letter;

	char LETTER;

	for (letter = 97; letter <= 122; letter++)
	{
		putchar(letter);
	}

	for (LETTER = 65; LETTER <= 90; LETTER++)
	{
		putchar(LETTER);
	}

	putchar('\n');

	return (0);
}
