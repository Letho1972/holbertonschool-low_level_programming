#include <stdio.h>
/**
 * op_add - addition
 * @a: value num1
 * @b: value num2
 * Return: always nothing
 */
int op_add(int a, int b)
	{
		return (a + b);
	}
/**
 * op_sub - soustraction
 * @a: value num1
 * @b: value num2
 * Return: always nothing
 */
int op_sub(int a, int b)
	{
		return (a - b);
	}
/**
 * op_mul - multiplication
 * @a: value num1
 * @b: value num2
 * Return: always nothing
 */
int op_mul(int a, int b)
	{
		return (a * b);
	}
/**
 * op_div - division
 * @a: value num1
 * @b: value num2
 * Return: always nothing
 */
int op_div(int a, int b)
	{
		return (a / b);
	}
/**
 * op_mod - modulo
 * @a: value num1
 * @b: value num2
 * Return: always nothing
 */
int op_mod(int a, int b)
	{
		return (a % b);
	}
